class CCipher {
public:
	string decode(string cipherText, int shift) {
		string decipherText = "";
		for(int i=0;i<cipherText.size();i++) { 
			char c = ((cipherText[i]-'A'+26-shift)%26) + 'A';
			decipherText += c;
		}
		return decipherText;
		
	}
};


