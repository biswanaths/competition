class StreetParking {
public:
	int freeParks(string street) {
		int s = street.size();
		street = "*" + street; int count = 0 ;
		street = street + "**";		
		for(int i=1;i<s+1;i++) { 
			bool pos = true;
			if(street[i]=='D') pos = false;
			if(street[i]=='B') pos = false;
			if(street[i+1]=='B' || street[i+2]=='B') pos= false;
			if(street[i]=='S') pos = false;
			if(street[i-1] =='S' || street[i+1]=='S') pos= false;
			count += pos ?1:0;  
		}
		return count;
		
	}
};


